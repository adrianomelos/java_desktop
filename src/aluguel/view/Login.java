    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        UserRepository repository = new UserRepository();
        
        String email = txtEmail.getText();
        String password = txtSenha.getText();
        
       
            if(!email.equals("")){
                if(!password.equals("")){
                    User user = new User();
                    user.setEmail(email);                
                    user.setPassword(password);
                          
                    boolean a = repository.login(user);
                    if(a){
                         this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Usuario e/ou senha Inválidos");
                    }
                    
                }else{
                    JOptionPane.showMessageDialog(null, "O campo senha não pode ser vazio");
                }
            }else{
                JOptionPane.showMessageDialog(null, "O campo email não pode ser vazio");
            }                   
    }//GEN-LAST:event_btnLoginActionPerformed

    
